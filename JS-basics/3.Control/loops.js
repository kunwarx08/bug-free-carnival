// Loops

//for loop
console.log("for loop");
for(let i=1;i<=10;i++)
{
    if (i % 2 !== 0) //only odd numbers will be printed
    {console.log(i);} 
    
}

//while loop
console.log("while loop");
let i=1;
while(i<=10)
{
    if (i % 2 !== 0) //only odd numbers will be printed
    {console.log(i);} 
    i++;
}

//do while loop
console.log("do-while loop"); //rarely used
let j=1;
do{
    
    if (j % 2 !== 0) //only odd numbers will be printed
    {console.log(j);} 
    j++;
}while(j<=10);



